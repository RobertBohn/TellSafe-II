<H1>What Is TellSafe?</H1>
<p>Under <a href="<?php print BASE ?>/sox301.php">Section 301 of the Sarbanes-Oxley Act</a>, audit committees must establish procedures for receiving anonymous complaints regarding their company's accounting practices. 
<p>TellSafe is an online service that helps audit committees comply with Section&nbsp;301.
<H1>How Does TellSafe Work?</H1>
<p>TellSafe provides an independent means of managing the receipt, retention, and confirmation of anonymous complaints.
<p>Employees of subscribing companies can register anonymous complaints on our secure website, by calling our 24-hour toll-free hotline, or through the mail.
